export function getIntegrationBySlug(slug: string): Integration | undefined {
  return integrations.find((integration) => integration.slug === slug);
}

export function getIntegrationsByCategory(
  category: string
): Integration[] {
  return integrations.filter(
    (integration) => integration.category === category
  );
}

export function getFeaturedIntegrations(): Integration[] {
  return integrations.filter((integration) => integration.featured);
}

export function getComingSoonIntegrations(): Integration[] {
  return integrations.filter((integration) => integration.comingSoon);
}
